const marks = 80;

if(marks >= 80){
    console.log(marks, "A+");
}else if(marks >= 70){
    console.log(marks, "A");
}else if(marks >= 60){
    console.log(marks, "A-");
}else if(marks >= 50){
    console.log(marks, "B");
}else if(marks >= 40){
    console.log(marks, "C");
}else if(marks >= 33){
    console.log(marks, "D");
}else{
    console.log(marks, "F");
}